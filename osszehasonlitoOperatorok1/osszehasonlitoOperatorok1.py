'''Összehasonlító operátorok:
<       kisebb
>       nagyobb
<=      kisebb, vagy egyenlő
>=      nagyobb, vagy egyenlő
==      egyenlő
!=      nem egyenlő
'''

szam_a = int(input('Mondj egy egész számot!\n>>>'))
szam_b = int(input('Mondj még egy egész számot!\n>>>'))
if szam_a == szam_b:
    print('A két szám egyenlő!')
else:
    print('A két szám nem egyenlő!')
